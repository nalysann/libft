#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*copy;

	len = ft_strlen(s1) + 1;
	if (!(copy = malloc(len)))
		return (NULL);
	return ((char *)ft_strncpy(copy, s1, len));
}
